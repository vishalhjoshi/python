sample_url = 'http://vishaljoshi.net'
print(sample_url)

# Reverse the url
print(sample_url[::-1])

# # Get the top level domain
print(sample_url[-4:])

# # Print the url without the http://
print(sample_url[7:])

# # Print the url without the http:// or the top level domain
print(sample_url[7:-4])